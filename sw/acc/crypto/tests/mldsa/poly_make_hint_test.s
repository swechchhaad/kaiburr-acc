 * Test for the make_hint subroutine of ML-DSA.
 */

.section .text.start

main:
  /* Prepare all-zero register. */
  bn.xor w31, w31, w31

  /* Call poly_make_hint. */
  la     x10, result
  la     x11, low_poly
  la     x2, high_bits
  bn.lid x0, 0(x2)
  jal x1, poly_make_hint

  ecall

